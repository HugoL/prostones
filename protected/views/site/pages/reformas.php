La <strong>rehabilitación</strong> de viviendas son acciones constructivas o 

edificatorias que se realizan para mejorar algunas de las siguientes condiciones: la 

accesibilidad (eliminación de barreras arquitectónicas), la habitabilidad, la protección 

contra la presencia de humedades, etc. Estas acciones se pueden realizar tanto en el 

interior de las viviendas como en las zonas comunes de los edificios residenciales.<br><br>

La plataforma proSton va a aportar, a precios muy competitivos, sus 

servicios de elaboración, medición y colocación de unos materiales de calidad, 

belleza y con un comportamiento a las exigencias que reciba, muy superior a otros 

productos de la construcción.<br><br>

Confie sus reformas y rehabilitaciones a proSton, es una organización con 

empresas eficientes (fabricas en donde salen los materiales y talleres cercanos a los 

clientes), que prestan sus servicios con rapidez, profesionalidad y a unos precios muy 

competitivos.
