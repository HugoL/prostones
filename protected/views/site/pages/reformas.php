En proSton denominamos <strong>reforma</strong> cuando no se transforman los espacios que componen una vivienda o edificio y solo se realizan obras de mantenimiento (cambio de solados, aplacados de las paredes, sustitución de piezas rotas, etc.)<br><br>
A pesar de la profunda crisis nmobiliaria, el parque de viviendas ha seguido aumentando (así como la antigüedad media de los inmuebles); lo que hace que la reforma (más ligada a la actual capacidad del gasto familiar que la adquisición de una vivienda nueva), sea un segmento estable donde 

refugiarse en estos tiempos de crisis.<br><br>

La necesidad de realizar reformas en el parque de viviendas que existe en la actualidad, generará trabajo para nuestro sector en los próximos años, y será mayor para aquellas empresas que trabajen con profesionalidad, que oferten precios accesibles y que sepan relacionarse con sus clientes utilizando las nuevas “armas” que nos brinda Internet. 
