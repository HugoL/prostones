		<h2>Acabados superficials de mármoles y granitos</h2>
		<p><strong>Cizallado / Granito.</strong><br>
		Es el acabado más natural de granito ya que presenta la rugosidad propia del material cuando se extrae de la naturaleza. Se consigue tronzando la piedra por presión mecánica.</p>
		<p><strong>Al corte de disco / Mármol y Granito.</strong><br>
		Es un acabado que deja la superficie lisa, muy porosa. Le da un acabado mate y un tono blanquecino a la piedra. Se consigue utilizando hilo o discos diamantados.
		</p>

		<p><strong>Al corte de Sierra / Mármol y Granito.</strong><br>
		Como consecuencia de dividir el bloque que se obtiene de la cantera en tablas, obtenemos este acabado que es el punto de partida para todos los demás. Las sierras utilizadas en este proceso dejan su huella en la piedra con una apariencia semi-rugosa e imperfecta, y al contrario del Pulido, ni deja ver la apariencia de la piedra, ni tapa el poro del material. Para resaltar un poco el color se puede además lavar al ácido.

		</p>
		<p>
		<strong>Apomazado / Mármol y Granito.</strong><br>
		Este acabado que se realiza en las piedras que presentan un nivel de cristalización alto, resalta su textura y color sin dejar ninguna marca visible, proporcionando un tono mas oscuro que el Serrado, pero sin llegar a tener brillo. Esto se consigue mediante un proceso de abrasión, que consiste en pasar piedras o lijas de diferente granulometría, hasta obtener el acabado deseado. El Apomazado es un paso previo antes del Pulido.</p>
		<p>
		<strong>Pulido / Mármol y Granito.</strong><br>
		El pulido es un acabado ideal para conseguir superficies lisas y brillantes. Esta técnica proporciona a la piedra mayor resistencia al ataque de agentes externos. Se consigue mediante abrasión.</p>
		<p><strong>Arenado / Mármol.</strong><br>
		El Arenado es un proceso que consiste en proyectar gránulos de abrasivo como Sílice o Corindón, mediante aire a alta presión contra la piedra. Con este proceso menos agresivo que el abujardado, conseguimos un acabado parecido a este, aunque con un poco mas de color y menor rugosidad.</p>
		<p><strong>Flameado / Mármol y Granito.</strong><br>
		El flameado es un acabado rugoso de aspecto vitrificado. Proporciona un alto grado de protección contra los agentes atmosféricos. Se consigue aplicando altas temperaturas en la superficie del marmol y del granito.</p>
		<p><strong>Abujardado / Mármol.</strong><br>
		Proporciona al material un acabado bello y muy antideslizante. El resultado de abujardar no siempre es el mismo, varia segun la dureza del material, la herramienta usada o el número de pasadas. Se consigue golpeando repetidamente, mediante una herramientas con puntas, la superficie a tratar.
		</p>
		<p><strong>Granallado / Granito</strong><br>
		Acabado similar al abujardado tradicional. Proporciona al granito una textura rugosa fina y uniforme, realza el tono natural de la piedra. Se consigue impactando granalla de acero inoxidable sobre la superficie de la piedra.</p>
		<p><strong>Envejecido / Mármol.</strong>
		Consiste en someter a la piedra a un proceso que pueden ser mecánico o químico. Con estos procesos se busca un acabado que imita el mármol, que ha sido sometido a las agresiones medioambientales, durante siglos lo dejan sin brillo y erosionado. Además del componente estético que da este innovador acabado, dota a la piedra de un cierto grado de rugosidad, que la hace apta para su uso en exteriores, ya que este acabado le da unas propiedades antideslizantes mayores que el Mármol pulido.</p>
