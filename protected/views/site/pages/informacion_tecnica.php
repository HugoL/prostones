		- Pavimentos (mármol principalmente). Plaquetas para solería de diversas dimensiones y espesores de 1 ó 2 cm, pulidas o sin pulir y, a veces, biseladas.<br>
		- Peldaños: con espesor mayor de 2 cm.<br>
		- A pie de cantera: adoquines, bordillos, mampuestos.<br>
