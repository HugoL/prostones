		<h2>Cómo medir su obra para una correcta adquisición de material</h2>
		
		<p>Recomendamos medir utilizando una cinta métrica, y tomar todas las medidas en metros, será más fácil calcular posteriormente los m². y m.l. Asegúrese que ha colocado bien las comas en las cifras para evitar que los cálculos sean incorrectos.<br> También es conveniente que haga un croquis de las habitaciones a medir, dibuje la forma de las mismas lo más semejante posible, incluyendo las irregularidades que pudiese tener.</p>

		<p><h3>1. Suelos</h3>
		<strong>1.1</strong> Baldosas:<br>
		Tomar en primer lugar la cota más larga de la superficie a calcular, después el lado menor en ambos extremos (si se apreciase una diferencia apreciable, apuntar la media entre ambas medidas). Una vez disponga de las dos medidas las multiplica y tendrá los m². de baldosa que va a necesitar.<br>
		Es habitual incrementar esta medida en un 10 % a la hora de hacer el pedido del material, ya que disponer de más material para realizar recortes, piezas que se rompen con la manipulación, alguna que tenga vetas o gabarros que no le agraden, etc. <br><br>

		1.2 -Rodapiés:<br>
		Sumar las cotas que tiene de las 4 paredes en cada habitación, el resultado es su perímetro y también los m.l. de rodapié que se va a necesitar.<br>
		Al igual que en las baldosas, es recomendable incrementar la totalidad del rodapié que le sale en sus cálculos en un 10 %.

		</p>
		<p><h3>2. Escaleras:</h3>
 
		<strong>2.1</strong> Huellas rectangulares:<br>

		- Necesitamos medir el largo y ancho de la baldosa, su espesor  y todas sus vistas (derecha, izquierda, ambas o solo el frontal) ademas de conocer las unidades necesarias.
 		</p>
 		<p>2.1.a Largo: Se mide el largo de la fábrica de ladrillo u hormigón que forma el escalón (se recomienda medir varios peldaños y utilizar el más largo); es conveniente que los laterales ya estén enlucidos (si no lo estuviesen, sumar 1 cm si se ve un canto y 2 cm si se ven los dos cantos cortos).<br>
		Una vez conocida esta medida hay que sumar 2 cm si va un canto corto pulido (vuelo lateral), o 4 cm si se ven los 2 cantos.<br>
		Si la huella está colocada entre dos paredes, se recomienda quitar 0’5 cm. a la que menor longitud tenga y utilizar esta anotación para el resto de peldaños.</p>
		<p>2.1.b	Ancho: Para saber esta medida, tomaran primero el ancho del escalón de ladrillo u hormigón, se añadirán 2 cm para el vuelo frontal y otros 2 cm para la carga de las tabicas (si las tabicas tienen 3 cm. Se añadirán 3 cm. de carga).
 		</p>
		Espesor: dependerá del gusto del Cliente y del dinero que se quiera gastar (a mayor grosor, mayor coste).
Si va a colocar mármol le recomendamos que coloque las huellas de 3 cm. de espesor, en el caso de colocar granito puede colocarse de 2 o de 3 cm.
 
Vistas: apunte en su cuaderno el lateral de los peldaños que estará a la vista (en esta información siempre deberá considerar que está subiendo la escalera). Debe dar este dato a la hora de hacer el Pedido, ya que además de pulir el canto frontal (labor que se hace siempre), se deberán pulir los laterales “vistos”.
 
Unidades: se debe apuntar las huellas a suministrar, es habitual pedir alguna pieza de más por si se rompe en el movimiento de la obra o durante la colocación.
		

