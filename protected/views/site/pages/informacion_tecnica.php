		<p>Aunque la utilización de la Piedra Natural por el hombre en todo el mundo tiene una larga historia, su consideración como actividad industrial es todavía muy reciente. En España, fue a partir de los años 60 cuando comenzó a convertirse en un importante sector de la industria minera, alcanzando el mayor desarrollo en la década de los 80, con una gran
		velocidad de crecimiento.</p>
		<p>
		Las Piedras Naturales, de mayor interés comercial y económico, son aquellas que por sus características de vistosidad, físico-mecánicas y aptitud para el pulido, en el caso de la pizarra se considera la facilidad para el lajado, constituyen la materia prima que ha dado lugar al desarrollo de la llamada Industria de la Piedra Natural. Atendiendo a estos criterios se conocen mundialmente los tres grupos denominados genéricamente Granitos, Mármoles y Pizarras, también llamadas Rocas Ornamentales por el valor estético que normalmente lleva aparejado su empleo.
		</p>
		<p>
		<strong>Granitos.</strong> <em>(UNE.22.170-85).</em><br> Se entiende por granito ornamental el conjunto de rocas ígneas compuestas por  diversos minerales (fundamentalmente por cristales de cuarzo, feldespatos y micas, en distintas proporciones que contribuyen a su clasificación, y que le confieren una textura granuda (Figura 6.1.2)), que se explotan en forma de bloques de naturaleza coherente y que se utilizan en la construcción para decoración, es decir, aprovechan sus cualidades estéticas, una vez elaboradas, con procedimientos tales como aserrado, pulido, tallado, esculpido, etc. Suelen ser muy homogéneos, de gran dureza y resistencia a las alteraciones.</p><p> El granito cristaliza a partir de magma enfriado muy lentamente. Es más duro que el mármol y las calizas, lo que dificulta mucho la extracción y posterior transformación.
		</p>
		<p>
		<strong>Mármoles y calizas marmóreas.</strong><em> (UNE.22.180-85).</em><br> Esta definición es aplicable al conjunto de rocas constituidas fundamentalmente por minerales carbonatados de dureza 3-4 (calcita, dolomita, etc.). Esta denominación incluye los mármoles ropiamente dichos, que son rocas metamórficas compuestas esencialmente de calcita o de dolomita.</p><p> Es una roca compacta formada a partir de rocas calizas que, sometidas a elevadas temperaturas y presiones alcanzan un alto grado de cristalización. El componente básico del Mármol es el carbonato cálcico cuyo contenido supera el 90%, los demás componentes son considerados impurezas, siendo estas las que nos dan gran variedad de colores en los mármoles, y definen su características físicas (Figura 6.1.3). Tras un proceso de pulido por abrasión el mármol alcanza alto nivel de brillo natural, es decir sin ceras ni componentes químicos.</p><p>
		Las <em>calizas</em> denominadas marmóreas son rocas carbonatadas, frecuentemente recristalizadas, compactas, de grano fino, normalmente con vetas de calcita e impurezas que proporcionan colores variados y, a veces, con inclusión de fósiles.	
		</p>
		<p>
		El <em>travertino</em> es una roca calcárea sedimentaria de depósito químico con estructura vacuolar, marcadamente laminada.
		</p>
		<p>
		Las <em>serpentinas</em>, aunque no son rocas carbonatadas, ya que proceden del metamorfismo de rocas básicas y ultrabásicas, se consideran tradicionalmente dentro de este grupo, hasta el punto que se les llega a conocer por mármol verde.
		</p>
		<p>
		<strong>Pizarras.</strong><em> (UNE.22.190-85).</em><br> Los elementos tabulares de pizarras son fragmentos de roca cuyas principales caras son esquistosidad natural de este tipo de rocas. La utilización fundamental de la pizarra es la de cubierta exterior de tejados.Las pizarras son rocas metamórficas, por lo general, de grano fino, cuya propiedad principal es que poseen unos planos de foliacion muy desarrollados, debida a la orientación planar de sus minerales principales, que constituye la esquistosidad,. Esto permite, que mediante procesos manuales de hienda o exfoliacion a favor de estos planos, se puedan obtener placas de espesores milimetricos y de caras muy lisas.</p><p>
		Estas rocas, proceden de sedimentos o rocas arcillosas preexistentes, que posteriormente fueron afectadas por movimientos orogenicos, que aportaron presiones y temperaturas en un regimen de metamorfismo de muy bajo grado y las transformo en el estado y forma que presentan actualmente.</p><p>
		La textura predominante en estas rocas es la lepidoblastica, observandose una masa de minerales filitosos con una orientacion paralela muy definida. A veces destacan algunos agregados minerales de mayor grosor dispersos en la matriz mayoritaria de grano fino.</p><p>
		La pizarra suele ser de color negro azulado o negro grisáceo, pero existen variedades rojas, verdes y otros tonos. Debido a su impermeabilidad, la pizarra se utiliza en la construcción de tejados, como piedra de pavimentación e incluso para fabricación de elementos decorativos.</p><p>
		Se desarrolla una fábrica planar en la roca denominada foliación y las rocas suelen fracturarse siguiendo los planos de foliación.
		</p>
		<p>
		Además de las rocas ya definidas, granitos, mármoles y pizarras, en las que se han incluido rocas de carácter bastante heterogéneo, existen otras tales como areniscas, calizas no pulimentables, cuarcitas, basaltos, alabastro, etc. La mayor parte se dedican a trabajos de cantería y se emplean en proyectos urbanos que siguen tradiciones locales y con presencia destacada en el patrimonio histórico artístico. Aunque estos tipos de rocas normalmente no adquieren brillo mediante pulimento, sí son susceptibles de otro tipo de acabado superficial como abujardado, escarfilado,flameado, etc.
			

		</p>















