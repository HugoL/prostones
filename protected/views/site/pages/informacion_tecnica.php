		<p>
		- En función del grado de elaboración los productos de la piedra natural se clasifican en los siguientes grupos:<br><br>
		<strong>a.-</strong> Material bruto.<br>
		- Bloques de forma regular e irregular o bolos.<br><br>
		<strong>b.-</strong> Material semietaborado.<br>
		- Bloques escuadrados. <br>
