<p>
Las rocas ornamentales (como producto natural), estan sujetas a variaciones de tonalidad y alteraciones de textura; que no son defecto del material, sino capricho de la naturaleza. Este factor contribuye a dar a su proyecto un carácter único e irrepetible, no atendiéndose reclamaciones por este motivo.<br><br>

A pesar del control que realizamos en Origen y al embalaje utilizado, durante el transporte pueden ocasionarse incidencias que afecten a un material o también que se reciba un producto que no se corresponde al <em>pedido</em>; por ello es posible realizar devoluciones.<br><br>

En caso de recibir un material con algún defecto, proSton procederá (según corresponda), a la reparación, sustitución,  o rebaja del precio (gestiones que serán gratuitas para el Cliente).<br><br>

<strong>-Nos podemos encontrar ante varios casos:</strong><br><br>

<strong>Primero.-</strong> En caso de que el Cliente considere que le ha llegado un material con algún defecto, debe enviarnos un eMail explicando la incidencia y adjuntando fotografías que respalden la reclamación. En un breve plazo de tiempo, nos pondremos en contacto con usted y le ofreceremos dos posibilidades:<br><br>

Volver a enviarte el material en perfecto estado. Si eliges esta opción, se te enviará de nuevo las piezas defectuosas y se recogerán las que no le sirvan. Esto no tendría ningún coste adicional para usted, proSton comprobará previamente su estado.<br><br>
Devolver todo el <em>pedido</em> y que se te reembolse el dinero. Si se elige esta opción, deberá embalar perfectamente al material para que podamos pasar a recogerlos.. Una vez que el fabricante reciba el producto y haya comprobado su estado, procederemos al reembolso del importe incluyendo los gastos de devolución a través de transferencia bancaria.<br><br>


<strong>Segundo.-</strong> Piezas que, por error, no corresponden al <em>pedido</em> realizado. Para efectuar la devolución, deberá ponerse en contacto con nosotros indicándonos el motivo de la devolución. Posteriormente, le enviaremos un eMail ofreciendole dos posibilidades:<br><br>

Enviar el <em>pedido</em> correcto. Se le enviará las piezas que corresponden a su <em>pedido</em>. Esta opción no tendrá ningún coste adicional para usted.<br><br>

Devolver el material y que se le reembolse el dinero. Mandaremos a recoger el producto que deberá encontrarse perfectamente embalado, cuando lo recibamos en la fábrica de Origen y se haya supervisado, se le reembolsará tanto el importe como los gastos de envío de la devolución.<br><br>


