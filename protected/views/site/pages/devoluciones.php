Las rocas ornamentales (como producto natural), estan sujetas a variaciones de tonalidad y alteraciones de textura; que no son defecto del material, sino capricho de la naturaleza. Este factor contribuye a dar a su proyecto un carácter único e irrepetible, no atendiéndose reclamaciones por este motivo.

A pesar del control que realizamos en Origen y al embalaje utilizado, durante el transporte pueden ocasionarse incidencias que afecten a un material o también que se reciba un producto que no se corresponde al PEDIDO; por ello es posible realizar devoluciones.

En caso de recibir un material con algún defecto, proSton procederá (según corresponda), a la reparación, sustitución,  o rebaja del precio (gestiones que serán gratuitas para el Cliente).

Nos podemos encontrar ante varios casos:

Primero.- En caso de que el Cliente considere que le ha llegado un material con algún defecto, debe enviarnos un eMail explicando la incidencia y adjuntando fotografías que respalden la reclamación. En un breve plazo de tiempo, nos pondremos en contacto con usted y le ofreceremos dos posibilidades:

Volver a enviarte el material en perfecto estado. Si eliges esta opción, se te enviará de nuevo las piezas defectuosas y se recogerán las que no le sirvan. Esto no tendría ningún coste adicional para usted, proSton comprobará previamente su estado.
Devolver todo el PEDIDO y que se te reembolse el dinero. Si se elige esta opción, deberá embalar perfectamente al material para que podamos pasar a recogerlos.. Una vez que el fabricante reciba el producto y haya comprobado su estado, procederemos al reembolso del importe incluyendo los gastos de devolución a través de transferencia bancaria.


Segundo.- Piezas que, por error, no corresponden al PEDIDO realizado. Para efectuar la devolución, deberá ponerse en contacto con nosotros indicándonos el motivo de la devolución. Posteriormente, le enviaremos un eMail ofreciendole dos posibilidades:

Enviar el PEDIDO correcto. Se le enviará las piezas que corresponden a su PEDIDO. Esta opción no tendrá ningún coste adicional para usted.

Devolver el material y que se le reembolse el dinero. Mandaremos a recoger el producto que deberá encontrarse perfectamente embalado, cuando lo recibamos en la fábrica de Origen y se haya supervisado, se le reembolsará tanto el importe como los gastos de envío de la devolución.


