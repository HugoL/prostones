<h2>Politica de cookies</h2>

<p>Una cookie es un pequeño fichero de texto que un servidor de páginas Web almacena en la unidad de disco duro del Usuario. Las cookies no pueden por sí misma identificar a una persona (no hacen referencia a ningún dato de carácter personal), tampoco se pueden ejecutar como código o contener virus.<br><br>

En proSton.es utilizamos las cookies exclusivamente para mejorar su experiencia en nuestras páginas; para almacenar sus preferencias de usuario (idioma y opciones predeterminadas); como medida de seguridad (evitando el uso fraudulento de credenciales de inicio de sesión).<br><br>

 Así mismo, utilizamos las siguientes cookies de terceros:<br><br>
Google Analytics: es una herramienta gratuita de análisis web de Google que principalmente permite que los propietarios de sitios web conozcan cómo interactúan los usuarios con su sitio web.  Estas cookies no le identifican ni guardan sus datos privados, sino que únicamente recogen información de la navegación que hacen los usuarios de forma anónima por proSton.es, para hacer un seguimiento estadístico de la misma.<br><br>

Redes sociales (Twitter, Facebook, Linkedin, YouTube, Pinterest y Google+): las principales redes sociales utilizan las cookies para facilitar las opciones de compartición de artículos y páginas. Estas cookies permiten el buen funcionamiento de los distintos widgets sociales que tenemos en nuestro sitio. Las condiciones de utilización de estas cookies y la información recopilada se regula por la política de privacidad de la plataforma social correspondiente.</p>


