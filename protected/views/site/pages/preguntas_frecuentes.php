<strong>¿Que rocas ornamentales se comercializarán en proSton.es ?</strong><br>
Todas, mármoles, granitos, calizas, areniscas, travertinos, cuarcitas, filitas, etc…, pero solo trabajamos con productos 100% naturales. 
Creemos que las ventajas de la piedra natural sobre la artificial, que vendidas a unos precios muy competitivos, las hará prevalecer sobre sus imitaciones.

