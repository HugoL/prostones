<br><br>

<strong>¿Se puede solicitar a proSton otros formatos o acabados diferentes a los incluidos en el sitio web?</strong>
Sí, los formatos y acabados que aparecen en nuestra web son los mas habituales, pero en proSton.es tiene un enlace para que los Usuarios nos soliciten otros diferentes, siempre que esto sea posible trataremos de satisfacerle. 
Hay que tener presente que, dependiendo de las características físico-mecánicas del material o de las maquinas que posean las fábricas que los elaboran, algunos acabados o formatos pueden ser imposibles de realizar.<br><br>

<strong>¿Pueden solicitar a proSton trabajos a medida?</strong><br>
